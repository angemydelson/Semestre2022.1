    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        DBController db = new DBController("dic.db");
        
        String palavra = edtPalavra.getText();
        String sinonimo = edtSinonimo.getText();
        
        try {
            
            db.conectar();
            db.salvarPalavra(palavra, sinonimo);
            db.desconectar();
            
            carregaDados();
            
            JOptionPane.showMessageDialog(this, "Informação cadastrada com sucesso!");
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }        
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void carregaDados() {
        DBController db = new DBController("dic.db");
        
        try {
            db.conectar();
            ResultSet rset = db.carregaPalavras();
            
            DefaultTableModel model = new DefaultTableModel(new String[]{"Palavra", "Sinonimo"}, 0);
            
            while (rset.next()) {
                model.addRow(new Object[]{rset.getString("palavra"), rset.getString("sinonimo")});
            }
            
            tblDados.setModel(model);
            
            db.desconectar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }
    
